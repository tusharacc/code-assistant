class ProjectContext:
    """
    Manages the code_assistant.md file in a project directory.

    Parameters
    ----------
    directory : Path | None
        Project root.  Defaults to Path.cwd().
    """

    def __init__(self, directory: Path | None = None) -> None:
        from .config import config
        self._dir = directory or Path.cwd()
        self._path = self._dir / config.project_context_file
        self._scanner = ProjectScanner()

    # ------------------------------------------------------------------
    # Basic accessors
    # ------------------------------------------------------------------

    def exists(self) -> bool:
        return self._path.exists()

    def read(self) -> str:
        try:
            return self._path.read_text(encoding="utf-8")
        except Exception:
            return ""

    # ------------------------------------------------------------------
    # ensure() — silent first-run discovery
    # ------------------------------------------------------------------

    def ensure(self) -> bool:
        """
        Generate code_assistant.md if it doesn't exist yet.

        Returns True if the file was created, False if it already existed.
        Silent — no console output.
        """
        if self._path.exists():
            return False

        result = self._scanner.scan(self._dir)
        content = self._render_initial(result)
        self._write(content)
        return True

    # ------------------------------------------------------------------
    # update_from_pipeline() — post-pipeline regeneration
    # ------------------------------------------------------------------

    def update_from_pipeline(
        self,
        state: "PipelineState",
        task: str,
    ) -> None:
        """
        Fully regenerate code_assistant.md after a pipeline run.

        Preserves the Work History section (appends a new row).
        Preserves the Open Requirements section if one exists.
        """
        existing = self.read()
        prev_history_rows = _extract_work_history(existing)

        # Attempt discovery; fall back to bare minimum if it fails
        try:
            discovery = self._scanner.scan(self._dir)
        except Exception:
            discovery = DiscoveryResult(
                name=self._dir.name,
                project_type="other",
                description="",
                language="Unknown",
                package_manager="unknown",
                test_runner="unknown",
            )

        verdict = _parse_test_verdict(state.test_results)
        criteria_pairs = _parse_acceptance_criteria(
            state.acceptance_criteria, state.test_results
        )
        files_written = _extract_files_written(state.impl_history)
        arch_summary = _summarise_arch_plan(state.arch_plan)

        # Build new history row
        today = date.today().isoformat()
        short_task = task[:80].replace("|", "\\|")
        new_row = f"| {today} | {short_task} | {verdict} |"
        history_rows = prev_history_rows + [new_row]

        # Preserve Open Requirements from previous file
        open_req = self._extract_section(existing, "Open Requirements")

        content = self._render_pipeline(
            discovery=discovery,
            arch_summary=arch_summary,
            files_written=files_written,
            verdict=verdict,
            criteria_pairs=criteria_pairs,
            history_rows=history_rows,
            open_requirements=open_req,
        )
        self._write(content)

    # ------------------------------------------------------------------
    # update_from_spec() — post-spec update
    # ------------------------------------------------------------------

    def update_from_spec(
        self,
        spec_path: Path,
        spec_content: str,
    ) -> None:
        """
        Update the Open Requirements section after ca --spec /finalize.

        All other sections are preserved from the existing file.
        """
        existing = self.read()

        criteria = _parse_spec_criteria(spec_content)

        if not criteria:
            open_req_content = f"See {spec_path.name} for full spec."
        else:
            lines = [f"From `{spec_path.name}`:"]
            for c in criteria:
                lines.append(f"- [ ] {c}")
            open_req_content = "\n".join(lines)

        # Replace (or append) the Open Requirements section
        if "## Open Requirements" in existing:
            new_content = re.sub(
                r"(## Open Requirements\s*\n)(.*?)(\Z|(?=\n## ))",
                f"\\1{open_req_content}\n",
                existing,
                flags=re.DOTALL,
            )
        else:
            new_content = existing.rstrip() + f"\n\n## Open Requirements\n\n{open_req_content}\n"

        self._write(new_content)

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _write(self, content: str) -> None:
        """Atomic write: write to .tmp then rename."""
        self._path.parent.mkdir(parents=True, exist_ok=True)
        tmp = self._path.with_suffix(".md.tmp")
        tmp.write_text(content, encoding="utf-8")
        tmp.replace(self._path)

    def _extract_section(self, content: str, section_name: str) -> str:
        """Extract the body of a ## section from a markdown string."""
        m = re.search(
            rf"## {re.escape(section_name)}\s*\n(.*?)(\Z|(?=\n## ))",
            content,
            re.DOTALL,
        )
        return m.group(1).strip() if m else ""

    def _render_initial(self, r: DiscoveryResult) -> str:
        """Render the initial code_assistant.md (first-run, no build info yet)."""
        lines = [
            f"# Project: {r.name}",
            "",
            "> Auto-generated by code-assistant. "
            "Do not edit manually — it will be overwritten after each pipeline run.",
            "",
            "## Project",
            "",
            f"- **Type**: {r.project_type.title()} ({_project_type_file(r.project_type)})",
            f"- **Language**: {r.language}",
        ]
        if r.description:
            lines.append(f"- **Description**: {r.description}")
        lines += [
            f"- **Package manager**: {r.package_manager}",
            f"- **Test runner**: {r.test_runner}",
        ]
        if r.entry_points:
            lines.append(f"- **Entry points**: {', '.join(f'`{e}`' for e in r.entry_points[:5])}")
        if r.key_dependencies:
            lines.append(f"- **Key dependencies**: {', '.join(r.key_dependencies)}")
        lines.append("")

        if r.module_layout:
            lines += ["## Module Layout", ""]
            for d in r.module_layout:
                lines.append(f"- {d}/")
            lines.append("")

        if r.key_files:
            lines += ["## Key Files", ""]
            for f in r.key_files:
                lines.append(f"- {f}")
            lines.append("")

        lines += [
            "## Current Status",
            "",
            "Not yet built by code-assistant.",
            "",
            "## Work History",
            "",
            "| Date | Task | Outcome |",
            "|------|------|---------|",
            "",
            "## Open Requirements",
            "",
            "*(populated after `ca --spec /finalize`)*",
            "",
        ]
        return "\n".join(lines)

    def _render_pipeline(
        self,
        discovery: DiscoveryResult,
        arch_summary: str,
        files_written: list[str],
        verdict: str,
        criteria_pairs: list[tuple[str, str]],
        history_rows: list[str],
        open_requirements: str,
    ) -> str:
        """Render code_assistant.md after a pipeline run."""
        lines = [
            f"# Project: {discovery.name}",
            "",
            "> Auto-generated by code-assistant. "
            "Do not edit manually — it will be overwritten after each pipeline run.",
            "",
            "## Project",
            "",
            f"- **Type**: {discovery.project_type.title()} "
            f"({_project_type_file(discovery.project_type)})",
            f"- **Language**: {discovery.language}",
        ]
        if discovery.description:
            lines.append(f"- **Description**: {discovery.description}")
        lines += [
            f"- **Package manager**: {discovery.package_manager}",
            f"- **Test runner**: {discovery.test_runner}",
        ]
        if discovery.entry_points:
            lines.append(
                f"- **Entry points**: {', '.join(f'`{e}`' for e in discovery.entry_points[:5])}"
            )
        if discovery.key_dependencies:
            lines.append(f"- **Key dependencies**: {', '.join(discovery.key_dependencies)}")
        lines.append("")

        if discovery.module_layout:
            lines += ["## Module Layout", ""]
            for d in discovery.module_layout:
                lines.append(f"- {d}/")
            lines.append("")

        if arch_summary:
            lines += ["## Architecture", ""]
            lines.append(arch_summary)
            lines.append("")

        if files_written:
            lines += ["## Key Files", ""]
            for f in files_written:
                lines.append(f"- {f}")
            lines.append("")

        # Current Status
        today = date.today().isoformat()
        status_icons = {"PASS": "✓", "FAIL": "✗", "PARTIAL": "~", "UNKNOWN": "?"}
        icon = status_icons.get(verdict, "?")
        if verdict == "PASS":
            status_line = f"**{icon} PASS** — all acceptance criteria passed ({today})"
        elif verdict in ("FAIL", "PARTIAL"):
            status_line = f"**{icon} {verdict}** — some criteria failed ({today})"
        else:
            status_line = f"**{icon} {verdict}** — outcome unknown ({today})"

        lines += ["## Current Status", "", status_line, ""]

        if criteria_pairs:
            lines += ["## Acceptance Criteria", ""]
            lines += ["| Criterion | Result |", "|-----------|--------|"]
            for criterion, result in criteria_pairs:
                safe = criterion.replace("|", "\\|")
                lines.append(f"| {safe} | {result} |")
            lines.append("")

        # Work History
        lines += ["## Work History", ""]
        lines += ["| Date | Task | Outcome |", "|------|------|---------|"]
        for row in history_rows:
            lines.append(row)
        lines.append("")

        # Open Requirements
        lines += ["## Open Requirements", ""]
        if open_requirements:
            lines.append(open_requirements)
        else:
            lines.append("*(none)*")
        lines.append("")

        return "\n".join(lines)


def _project_type_file(project_type: str) -> str:
    """Return the primary manifest file name for a project type."""
    return {
        "python": "pyproject.toml",
        "node": "package.json",
    }.get(project_type, "unknown")
